package polymorphism01;

class LgTV {
	public void on() {
		System.out.println("LgTV - 전원 ON");
	}
	public void off() {
		System.out.println("LgTV - 전원 OFF");
	}
	public void soundUp() {
		System.out.println("LgTV - 소리크게~~");
	}
	public void soundDown() {
		System.out.println("LgTV - 소리작게~~");
	}
	
}
